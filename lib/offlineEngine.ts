function interpolateWHO(table: Record<number, number[]>, age: number): number[] | null {
  if (table[age]) return table[age];
  const keys = Object.keys(table).map(Number).sort((a, b) => a - b);
  const lower = keys.filter(k => k <= age).pop();
  const upper = keys.find(k => k > age);
  if (lower == null || upper == null) return null;
  const lo = table[lower], hi = table[upper];
  const frac = (age - lower) / (upper - lower);
  return lo.map((v, i) => v + frac * (hi[i] - v));
}

function interpolateWFH(table: Record<number, number[]>, heightCm: number): number[] | null {
  const rounded = Math.round(heightCm);
  if (table[rounded]) return table[rounded];
  const keys = Object.keys(table).map(Number).sort((a, b) => a - b);
  const lower = keys.filter(k => k <= heightCm).pop();
  const upper = keys.find(k => k > heightCm);
  if (lower == null || upper == null) return null;
  const lo = table[lower], hi = table[upper];
  const frac = (heightCm - lower) / (upper - lower);
  return lo.map((v, i) => v + frac * (hi[i] - v));
}

function classifyWAZ(weight: number, age: number, male: boolean): WHOCategory {
  const ref = interpolateWHO(male ? WHO_WAZ_MALE : WHO_WAZ_FEMALE, age);
  if (!ref) return 'normal';
  if (weight < ref[3]) return 'severely_underweight';
  if (weight < ref[2]) return 'underweight';
  return 'normal';
}

function classifyLAZ(height: number, age: number, male: boolean): WHOCategory {
  const ref = interpolateWHO(male ? WHO_LAZ_MALE : WHO_LAZ_FEMALE, age);
  if (!ref) return 'normal';
  if (height < ref[3]) return 'severely_stunted';
  if (height < ref[2]) return 'stunted';
  return 'normal';
}

function classifyWLZ(weight: number, height: number, male: boolean): WHOCategory {
  const ref = interpolateWFH(male ? WHO_WFH_MALE : WHO_WFH_FEMALE, height);
  if (!ref) return 'normal';
  if (weight < ref[0]) return 'severely_wasted';
  if (weight < ref[1]) return 'wasted';
  if (weight > ref[5]) return 'overweight';
  return 'normal';
}

function interpretMUAC(muac: number, age: number): 'sam' | 'mam' | 'normal' {
  if (age < 6) return 'normal';
  if (muac < 11.5) return 'sam';
  if (muac < 12.5) return 'mam';
  return 'normal';
}

function whoEmoji(cat: WHOCategory): string {
  if (cat.startsWith('severely_')) return '🔴';
  if (cat !== 'normal') return '🟡';
  return '🟢';
}

function whoLabel(cat: WHOCategory): string {
  const m: Record<WHOCategory, string> = {
    normal: 'Normal',
    underweight: 'Berat kurang (BB/U)',
    severely_underweight: 'Berat sangat kurang',
    stunted: 'Pendek (TB/U)',
    severely_stunted: 'Sangat pendek',
    wasted: 'Kurus (BB/TB)',
    severely_wasted: 'Sangat kurus',
    overweight: 'Risiko lebih berat',
  };
  return m[cat];
}

